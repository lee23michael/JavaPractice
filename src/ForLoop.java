public class ForLoop
{
    public static void main(String[] args)
    {
        // for loop
        for(int i = 0 ; i < 12; i++) //(initialize; condition ; increment)
        {
            System.out.print(i);
            System.out.printf("%d\t",i); // %d = an integer, \t = add a tab
            // format specifiers http://alvinalexander.com/programming/printf-format-cheat-sheet
        }
    }
}
