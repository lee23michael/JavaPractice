public class ForLoop
{
    public static void main(String[] args)
    {
        //for loop
        for(int i = 0 ; i < 15 ; i++) //(initialization; condition, incrementation )
        {
            System.out.print(i);
            System.out.printf("%d\t",i); // %d = integer, \t = add a tab
        }

        // format specifiers http://alvinalexander.com/programming/printf-format-cheat-sheet
    }
}
